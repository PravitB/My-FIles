public class multable {
    public static void main(String[] args) {
        int n = 5; // Change this to generate a different multiplication table
        System.out.println("Multiplication table for " + n + ":");
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }
    }
}
